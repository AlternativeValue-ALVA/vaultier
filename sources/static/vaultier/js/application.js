/**************************************************
 **************************************************
 * Ember
 **************************************************
 **************************************************
 */

Ember.FEATURES["query-params"] = true
Ember.MODEL_FACTORY_INJECTIONS = true;

/**************************************************
 **************************************************
 * Application
 **************************************************
 **************************************************
 */

Vaultier = Ember.Application.create({
    LOG_TRANSITIONS: true,
    // LOG_TRANSITIONS_INTERNAL: true,

    ready: function () {

        /**************************************************
         **************************************************
         * Initialize config
         **************************************************
         **************************************************
         */
        InitializeConfig(this);


        LGTM.configure('defer', Ember.RSVP.defer);

        /**************************************************
         **************************************************
         * Notifications
         **************************************************
         **************************************************
         */

        $.notify.defaults({
            className: 'success',
            style: 'bootstrap',
            position: 'bottom center',
            autoHideDelay: 7000,
            css: "vlt-notification"
        })

        /**************************************************
         **************************************************
         * Handlebars
         **************************************************
         **************************************************
         */
        Utils.HandlebarsHelpers.current().register();

        /**************************************************
         **************************************************
         * Cookies
         **************************************************
         **************************************************
         */
        $.cookie.json = true;

        /**************************************************
         **************************************************
         * Tooltips
         **************************************************
         **************************************************
         */

        $('body').tooltip({
            selector: '[data-toggle=tooltip]'
        });

        /**************************************************
         **************************************************
         * Sticky footer
         **************************************************
         **************************************************
         */

        setInterval(function() {
            var body = $('body').height();
            var win = $(window).height();
            var footer = $('#vlt-footer').height();
            if ( body + footer < win)  {
                $('#vlt-footer').css({position: 'fixed'})
            } else {
                $('#vlt-footer').css({position: 'relative'})
            }
        }, 500)

        /**************************************************
         **************************************************
         * Global UI bindings
         **************************************************
         **************************************************
         */

        // instead css overflow-y:scroll
//        var minHeight = function () {
//            $('html').css('height', $(window).height()+1);
//        }
//        $(window).resize(minHeight());
//        minHeight();

        this.keypressBindings();
        this.registerDI(this);

    }
});

Vaultier.deferReadiness();
